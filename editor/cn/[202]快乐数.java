class Solution {
    public boolean isHappy(int n) {
        int iter = n;
        HashSet<Integer> set = new HashSet<>();
        while (true) {
            if (set.contains(iter)) {
                return false;
            } else {
                set.add(iter);
            }
            iter = cal(iter);
            if (iter == 1) {
                return true;
            }
        }
    }

    public int cal(int n) {
        int sum = 0;
        while (n != 0) {
            int cur = n % 10;
            sum = sum + cur * cur;
            n = n / 10;
        }
        return sum;
    }
}
